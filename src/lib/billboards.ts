export type SurfaceFilter = "all" | "oriasplakat" | "orias" | "kis";

export function filterBillboards(
  items: Billboard[],
  typeFilter: SurfaceFilter,
  city: string
): Billboard[] {
  return items.filter((b) => {
    if (city !== "all" && b.city !== city) return false;
    if (typeFilter === "all") return true;
    const t = b.type.toLowerCase();
    if (typeFilter === "oriasplakat") return t.includes("óriásplakát");
    if (typeFilter === "orias") return t.includes("óriás felület");
    if (typeFilter === "kis") return t.includes("kis");
    return true;
  });
}
