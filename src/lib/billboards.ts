export function toMapBillboard(b: Billboard): MapBillboard {
  return {
    id: b.id,
    title: b.name,
    city: b.city,
    lat: b.lat,
    lng: b.lng,
    ots: b.ots,
    type: b.type,
    size: b.size,
    price: b.price,
    imageUrl: b.imageUrl,
    status: b.status,
    desc: b.desc,
  };
}

export const mapBillboards: MapBillboard[] = billboards.map(toMapBillboard);

